def luasPersegi(sisi):
    return sisi * sisi


def luas_persegi_panjang(panjang, lebar):
    return panjang * lebar


def luasSegitiga(alas, tinggi):
    return 1/2*(alas*tinggi)


def luasJajarGenjang(alas, tinggi):
    return alas*tinggi


def luasTrapesium(jumlahSisi, tinggi):
    return 1/2*(jumlahSisi*tinggi)


def luasLayangLayang(diagonal1, diagonal2):
    return 1/2*(diagonal1*diagonal2)


def luasBelahKetupat(diagonal1, diagonal2):
    return 1/2*(diagonal1*diagonal2)


def luasLingkaran(jariJari):
    return 3.14*(jariJari*jariJari)
